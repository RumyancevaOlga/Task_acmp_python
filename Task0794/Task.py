# Вася готовит инвентарь для ролевой игры. В игре должны принять участие n игроков,
# каждый из которых будет изображать персонажа фантастического мира. В процессе игры каждый персонаж будет
# обладать некоторым уровнем x, который представляет собой целое число от 1 до m.
#
# Для обозначения уровня планируется использовать специальные значки двух цветов. Белый значок обозначает
# один уровень, а красный значок – k уровней. Игрок, изображающий персонажа с уровнем x, должен иметь a белых
# значков и b красных значков, чтобы сумма (a + b*k) была равна x. При этом персонажу не разрешается иметь
# более чем (k – 1) белых значков.
#
# Значки для игры готовятся заранее, однако уровни персонажей заранее неизвестны. Для успешного проведения
# игры всем персонажам необходимо выдать соответствующее их уровням количество значков. Возникает вопрос:
# какое минимальное суммарное количество значков необходимо подготовить для успешного проведения игры при
# любых уровнях участвующих персонажей.
#
# Требуется написать программу, которая по заданным числам n, m и k вычисляет минимальное количество значков,
# которое необходимо подготовить для успешного проведения игры.

n, m, k = map(int, input().split())
result = n * (min(k - 1, m) + m // k)
print(result)
