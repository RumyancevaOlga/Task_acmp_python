# В эту субботу у Васи день рождения и через 15 минут к нему придут гости.
# Ему срочно надо вскипятить чай, для того чтобы напоить им гостей.
# У Васи дома есть много литровых чайников (можно считать, что их бесконечное количество),
# а розетка всего одна. Т.к. вода кипятится очень долго, за 15 минут она успеет вскипятиться максимум один раз.
# Но Вася – мальчик не промах, он достал из кладовки N тройников, в i-том тройнике ai разъемов.
# Теперь Вася ломает голову: как ему соединить тройники и воткнуть эту систему в розетку, чтобы максимизировать
# количество чайников, которые он сможет поставить кипятить.
#
# Ваша задача заключается в написании программы, которая определит максимальное число чайников,
# которые возможно использовать для кипячения чая, используя данные тройники.

n = int(input())
a_i = list(map(int, input().split()))
result = sum(a_i) - n + 1
print(result)
