# Система улиц Нью-Йоркского района Манхеттен весьма интересна. В Манхеттене есть n улиц, идущие с
# запада на восток (авеню), и m улиц, идущие с севера на юг (просто улицы). Ширина каждого авеню и
# каждой улицы равна d метров, а длина – k метров. При этом каждая улица пересекает каждый авеню и не имеет
# общих точек с другими улицами, а каждый авеню пересекает каждую улицу и не имеет общих точек с другими авеню.
#
# Разумеется, все авеню и улицы имеют асфальтовое покрытие. Дорожно-ремонтные службы интересуются: сколько
# квадратных метров асфальта уложено на все авеню и улицы. На перекрестках, без сомнения, асфальт уложен в один слой.
#
# Напишите программу, вычисляющую ответ на их вопрос.

n, m, d, k = map(int, input().split())
result = (n + m) * d * k - (n * m * d * d)
print(result)
