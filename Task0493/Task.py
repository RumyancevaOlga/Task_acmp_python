# «Морской бой» - игра для двух участников, в которой игроки по очереди называют координаты
# на неизвестной им карте соперника. Если у соперника по этим координатам имеется корабль,
# то корабль или его часть «топится», а попавший получает право сделать еще один ход.
# Цель игрока - первым поразить все корабли противника.
#
# «Морской бой» очень популярен среди учеников одной физико-математической школы.
# Ребята очень любят в него играть на переменах. Вот и сейчас ученики Иннокентий и Емельян начали новую партию.
#
# Правила, по которым ребята расставляют корабли перед началом партии, несколько отличаются от классических.
# Во-первых, игра происходит на поле размером N×M, а не 10×10. Во-вторых, число кораблей, их размер и
# форма выбираются ребятами перед партией - так играть намного интереснее.
#
# Емельян уже расставил все свои корабли, кроме одного однопалубного. Такой корабль занимает ровно одну клетку.
#
# Задана расстановка кораблей Емельяна. Найдите число способов поставить оставшийся однопалубный корабль.
# При этом учитывайте, что по правилам его можно ставить только в ту клетку, все соседние с которой не заняты.
# В этой задаче соседними считаются клетки, имеющие общую сторону.

count = 0
n, m = map(int, input().split())
list_ships = []

for i in range(n):
    list_ships.append(input())

for i in range(n):
    for j in range(m):
        if list_ships[i][j] == '.' and (i == 0 or list_ships[i - 1][j] == '.')\
                and (i == n - 1 or list_ships[i + 1][j] == '.')\
                and (j == 0 or list_ships[i][j - 1] == '.')\
                and (j == m - 1 or list_ships[i][j + 1] == '.'):
            count += 1

print(count)
