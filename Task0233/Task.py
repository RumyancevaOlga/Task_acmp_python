# Оргкомитет Московской городской олимпиады решил организовать обзорную экскурсию
# по Москве для участников олимпиады. Для этого был заказан двухэтажный автобус
# (участников олимпиады достаточно много и в обычный они не умещаются) высотой 437 сантиметров.
# На экскурсионном маршруте встречаются N мостов. Жюри и оргкомитет олимпиады очень обеспокоены тем,
# что высокий двухэтажный автобус может не проехать под одним из них. Им удалось выяснить точную высоту
# каждого из мостов. Автобус может проехать под мостом тогда и только тогда, когда высота моста превосходит
# высоту автобуса
# Помогите организаторам узнать, закончится ли экскурсия благополучно, а если нет, то установить, где произойдет авария.

n = int(input())
wight = list(map(int, input().split()))

for i in range(n):
    if wight[i] <= 437:
        print(f'Crash {i + 1}')
        break
else:
    print('No crash')
