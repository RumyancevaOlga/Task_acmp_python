# Маша недавно прочитала в книге «Теория чисел и предсказание будущего» о новом способе гадания.
# Способ заключается в следующем: надо выбрать целое положительное число N и посчитать количество чисел,
# меньших N и взаимно простых с ним. Это количество и будет результатом гадания.
#
# Поскольку гадать приходится достаточно часто, а процесс гадания достаточно трудоемок,
# Маша попросила вас написать программу, считающую результат гадания.
#
# Два числа называются взаимно простыми, если их наибольший общий делитель равен 1. Наибольшим общим
# делителем двух чисел a и b называется наибольшее целое положительное число, на которое делятся и a, и b.

import math

n = int(input())
result = 0
for i in range(1, n):
    if math.gcd(i, n) == 1:
        result += 1
print(result)
