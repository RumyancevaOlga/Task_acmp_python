# В теории формальных грамматик и автоматов (ТФГиА) важную роль играют так называемые
# контекстно-свободные грамматики (КС-грамматики). КС-грамматикой будем называть четверку,
# состоящую из множества N нетерминальных символов, множества T терминальных символов, множества P правил
# (продукций) и начального символа S, принадлежащего множеству N.
#
# Каждая продукция p из P имеет форму A –> a, где A нетерминальный символ (A из N), а a – строка, состоящая из
# терминальных и нетерминальных символов. Процесс вывода слова начинается со строки, содержащей только начальный
# символ S. После этого на каждом шаге один из нетерминальных символов, входящих в текущую строку, заменяется
# на правую часть одной из продукций, в которой он является левой частью. Если после такой операции получается
# строка, содержащая только терминальные символы, то процесс вывода заканчивается.
#
# Во многих теоретических задачах удобно рассматривать так называемые нормальные формы грамматик. Процесс
# приведения грамматики к нормальной форме часто начинается с устранения левой рекурсии. В этой задаче мы
# будем рассматривать только ее частный случай, называемый непосредственной левой рекурсией. Говорят, что
# правило вывода A –> R содержит непосредственную левую рекурсию, если первым символом строки R является A.
#
# Задана КС-грамматика. Требуется найти количество правил, содержащих непосредственную левую рекурсию.

n = int(input())
count = 0
for i in range(n):
    my_string = input()
    if my_string[0] == my_string[3]:
        count += 1
print(count)
