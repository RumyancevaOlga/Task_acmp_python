# Студент-первокурсник Антон никак не может понять странную формулу
#
# 1/2 + 1/3 + 1/4 + ... + 1/n + ... → бесконечность
#
# Хорошо, что у Антона есть друг Васька, который отлично знает математику.
# Вот и придумал Васька, как Антошке объяснить эту самую бесконечность.
# Взял колоду карт и стал раскладывать карты на столе. Если есть одна карта,
# она нависает над столом на 1/2 длины карты. Если есть две карты, то верхняя
# нависает над нижней на 1/2 длины карты, а нижняя - нависает над столом на 1/3 длины.
# В итоге получается нависание 1/2+1/3=5/6. А если у нас есть N карт,
# то длина нависающей части уже 1/2+1/3+1/4+...+1/(N+1).
#
# И стал Васька с Антоном в такую игру играть: Антон называет какое-нибудь число,
# а Вася называет минимальное количество карт, которое нужно положить, чтобы длина
# нависающей части была бы не меньше этого числа.
#
# Напишите программу, которая помогает Васе каждый раз давать правильный ответ.

count = 1
x = float(input())
i = 2
n = 1 / i

while n < x:
    i += 1
    n += 1 / i
    count += 1

print(f'{count} card(s)')
