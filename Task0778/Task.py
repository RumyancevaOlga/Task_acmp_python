# Летом Вася очень любил смотреть в окно. Напротив его дома расположился офис некоторой строительной
# фирмы. В течение всего месяца Вася наблюдал за его сотрудниками. Про каждый из 31 дня месяца он знает,
# сколько сотрудников пришло на работу. Ему также известно, что каждый из сотрудников берет ровно по 4 выходных в
# месяц.
#
# Теперь он ломает голову над загадкой – сколько всего сотрудников работает в этом офисе. Напишите программу,
# которая ответит Васе на этот вопрос.

list_staff = list(map(int, input().split()))
result = int(sum(list_staff) / 27)
print(result)
