# На бесконечной вправо и вверх шахматной доске находится ладья. Два игрока передвигают ее по очереди.
# За один ход разрешено сдвинуть ладью вниз или влево на произвольное (ненулевое) количество клеток так,
# чтобы ладья не покинула доску. Цель игры – переместить ладью в левый нижний угол, то есть клетку
# с координатами (1,1). Известно, что оба игрока придерживаются оптимальной стратегии. Игрок №1 ходит первым,
# при этом он обязан совершить хотя бы один ход. Если первый ход сделать нельзя, то определить победителя
# также невозможно. Требуется написать программу, которая найдет номер победившего игрока, либо определит,
# что этого сделать нельзя.

x, y = map(int, input().split())
if x == y == 1:
    print(0)
elif x == y:
    print(2)
else:
    print(1)
